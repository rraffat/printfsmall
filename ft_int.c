#include "ft_printf.h"

int	ft_int(int d)
{
	int	i;

	i = 0;
	if (d == -2147483648)
		return (i += ft_string ("-2147483648"));
	if (d < 0)
	{
		i += ft_putchar('-');
		d *= -1;
	}
	if (d < 10)
		i += ft_putchar(d + '0');
	else
	{
		i += ft_int(d / 10);
		i += ft_int(d % 10);
	}
	return (i);
}
