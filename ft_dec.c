#include "ft_printf.h"

int	ft_dec(unsigned int u)
{
	int	i;

	i = 0;
	if (u < 10)
		i += ft_putchar(u + '0');
	else
	{
		i += ft_dec(u / 10);
		i += ft_dec(u % 10);
	}
	return (i);
}
